package com.lewis.lzw03;

public class learningJava {
    int value;
    int age;

    public learningJava(int value, int age) {
        this.value = value;
        this.age = age;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
